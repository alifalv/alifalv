package com.legal.app.controller.model;

public class EffectivenessGrade {
	private int effectivenessGradeId;
	private String effectivenessGradeName;
	public int getEffectivenessGradeId() {
		return effectivenessGradeId;
	}
	public void setEffectivenessGradeId(int effectivenessGradeId) {
		this.effectivenessGradeId = effectivenessGradeId;
	}
	public String getEffectivenessGradeName() {
		return effectivenessGradeName;
	}
	public void setEffectivenessGradeName(String effectivenessGradeName) {
		this.effectivenessGradeName = effectivenessGradeName;
	}
}
